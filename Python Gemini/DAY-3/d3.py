"""
Write a function named calculate_area that accepts two arguments: length and width.

Inside the function, calculate the area (length multiplied by width).

The function should return the calculated area.

After defining the function, call it with a length of 10 and a width of 5, 
and store the result in a variable named my_area.

Finally, print the value of my_area.
"""

def calculate_area(length, width):
    area = length * width
    return area
print(calculate_area(10, 5))